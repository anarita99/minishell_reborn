#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dup;
	int		i;

	if(!str)
		return (NULL);
	i = 0;
	dup = malloc(sizeof(char) * (ft_strlen(str) + 1));
	if (dup == NULL)
		return (NULL);
	while (str[i] != '\0')
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
